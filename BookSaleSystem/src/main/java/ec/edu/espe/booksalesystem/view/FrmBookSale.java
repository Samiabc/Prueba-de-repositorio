    private void cmbBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbBookActionPerformed
        // TODO add your handling code here:
        if (cmbBook.getSelectedIndex() <= 0) {
            txtUnitPrice.setText("");
            cmbBook.setBorder(BorderFactory.createLineBorder(java.awt.Color.RED));
            return;
        }

        cmbBook.setBorder(BorderFactory.createEmptyBorder());

        String selectedTitle = cmbBook.getSelectedItem().toString();
        Book book = bookController.getBookByTitle(selectedTitle);

        if (book != null) {
            txtUnitPrice.setText(String.valueOf(book.getPrice()));
            txtUnitPrice.setEditable(false);
        }
    }//GEN-LAST:event_cmbBookActionPerformed

