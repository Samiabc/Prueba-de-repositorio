    private void clearForm() {
        cmbBook.setSelectedIndex(0);
        txtUnitPrice.setText("");
        spnQuantity.setValue(1);
    }

    private final BookController bookController = new BookController();
    
    private final SaleController saleController = new SaleController();

    private boolean validateQuantity() {
        int quantity = (int) spnQuantity.getValue();

        if (quantity <= 0) {
            spnQuantity.setBorder(BorderFactory.createLineBorder(java.awt.Color.RED));
            JOptionPane.showMessageDialog(
                    this,
                    "Quantity must be greater than zero",
                    "Invalid Quantity",
                    JOptionPane.ERROR_MESSAGE
            );
            return false;
        }

        spnQuantity.setBorder(BorderFactory.createEmptyBorder());
        return true;
    }

    private boolean validateForm() {
        boolean valid = true;

        if (cmbBook.getSelectedIndex() <= 0) {
            cmbBook.setBorder(BorderFactory.createLineBorder(java.awt.Color.RED));
            valid = false;
        } else {
            cmbBook.setBorder(BorderFactory.createEmptyBorder());
        }

        if (!validateQuantity()) {
            valid = false;
        }

        return valid;
    }

    private void loadBooks() {
        cmbBook.removeAllItems();
        cmbBook.addItem("Select a book...");

        for (Book book : bookController.getAllBooks()) {
            cmbBook.addItem(book.getTitle());
        }
    }
    
    private void configureSpinner() {
        javax.swing.SpinnerNumberModel model =
                new javax.swing.SpinnerNumberModel(1, 1, 100, 1);
        spnQuantity.setModel(model);
    }
